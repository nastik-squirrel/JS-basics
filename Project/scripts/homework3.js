function homework31() {
    let number = prompt('Input number:');
    let result = +number % 2;
    let text = '';
    switch (result) {
        case 0:
            text = `${number} is EVEN`;
            break;
        case 1:
            text = `${number} is ODD`;
            break;
        default:
            text = `"${number}" is not a number`;
            break;
    }

    alert(text);
}

